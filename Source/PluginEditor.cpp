void AudioClockEditor::resized()
{
    measureBox->setBounds( 39, 120, 75, 24 );
    measureLabel->setBounds(23, 96, 150, 24 );
    websiteLink->setBounds( 10, 155, 105, 20 );
}

void AudioClockEditor::comboBoxChanged( ComboBox* comboBoxThatHasChanged )
{
    if (comboBoxThatHasChanged == measureBox)
    {
        // normalize selected ID by number of elements to store parameter within 0..1 range (->Bitwig!)
        float param = (float)((float)measureBox->getSelectedId() / (float)measureBox->getNumItems());
        // set parameter in plugin processor and notify host
		getProcessor()->setParameterNotifyingHost(AudioClockAudioProcessor::PMeasure, param);
    }
}


// poll UI update flag which is set by DAW host when host changes measure parameter. update UI accordingly here
void AudioClockEditor::timerCallback(){
	AudioClockAudioProcessor* ourProcessor = getProcessor();
    if (ourProcessor->NeedsUiUpdate())
    {
        int selId = roundf( ourProcessor->getParameter(0) * (float)measureBox->getNumItems() );
        // init Plugin CHECK!! TODO
        if(selId == 0)
        {
            selId = 1;
        }
		measureBox->setSelectedId( selId );
 		ourProcessor->ClearUiUpdateFlag();
	}
}



